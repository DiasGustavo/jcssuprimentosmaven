    public Juiz preencherJuiz() {
        ///juiz = new Juiz();
        int status = cbStatus.getSelectedIndex();
        if(status == 0){
            ViewUtil.addMsgErro("NÃ£o foi selecionado nenhum status");
        }else{
            juiz.setStatus(status - 1);
        }

        juiz.setLogin(this.jtxLogin.getText());
        juiz.setSenha(this.jtxSenha.getText());
        juiz.setEmail(this.jtxEmail.getText());
        juiz.setStatus(this.cbStatus.getSelectedIndex());
        juiz.setNome(this.jtxNome.getText());
        juiz.setFuncao("juiz");

        ValidatorFactory factory = Validation.buildDefaultValidatorFactory();
        Validator validator = factory.getValidator();
        Set<ConstraintViolation<Juiz>> constraintViolation = validator.validate(juiz);

        if (!constraintViolation.isEmpty()) {
            String mensagem = "";
            for (ConstraintViolation<Juiz> erro : constraintViolation) {
                mensagem += erro.getMessage() + "\n";
                ViewUtil.addMsgErro(mensagem);
            }
            this.error = true;
        }

        return juiz;
    }


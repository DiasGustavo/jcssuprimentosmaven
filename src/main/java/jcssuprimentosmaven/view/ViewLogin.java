    public Jogador preencherUsuario() {
        jogador = new Jogador();
        jogador.setLogin(this.txUsuario.getText());
        jogador.setSenha(this.txSenha.getText());
        String funcao = (String) this.cbFuncao.getSelectedItem();
        int funcaoAnalisar = 0;
        if (funcao.equals("Participante")) {
            funcaoAnalisar = 1;
        }
        if (funcao.equals("Juiz")) {
            funcaoAnalisar = 2;
        }
        jogador.setFuncao(funcaoAnalisar);

        return jogador;
    }

    public void iniciaSistema(boolean inicio, Jogador usuario) {
        if (inicio) {
            this.dispose();
            viewPrincipal = new ViewPrincipal(usuario);
            viewPrincipal.setVisible(true);
            viewPrincipal.setJogador(usuario);

        } else {
            ViewUtil.addMsgErro("Login/senha incorretos!");
        }
    }


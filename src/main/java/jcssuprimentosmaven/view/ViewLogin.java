    private void cbFuncaoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cbFuncaoKeyPressed
        // TODO add your handling code here:
        if ((evt.getKeyCode() == KeyEvent.VK_ENTER)) {

            Jogador jogador = this.preencherUsuario();
            JogadorController jogadorController = new JogadorController();
            jogadorController.setJogadorCadastro(jogador);
            Jogador autentico = jogadorController.autenticarUsuario();
            if (autentico != null) {
                this.iniciaSistema(true, autentico);
            } else {
                this.iniciaSistema(false, null);
            }

        }
    }//GEN-LAST:event_cbFuncaoKeyPressed

